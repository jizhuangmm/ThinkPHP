	function send(){
		var name = $('#name').val();
		var phone = $('#phone').val();
		var province22 = $('#province').val();
		
		if(name=='' || phone=='' || province22==''){
			alert('请填写信息！');
			$('#form').attr('action','');
			return false;
		}
	
	} 
	
	
	//省市选择
	function provinces(){
        var province = $('#province').val();
        $.ajax({
			type:"POST",
			url:"<?php echo U('province');?>",
			dataType:"json",
			data:{'province':province},
			success:function(data){
				var ss='';
				for(var i=0; i<data.length; i++){
					ss+= "<option value='"+data[i]['city_name']+"'>"+data[i]['city_name']+"</option>";
				}
				$("#cityid").html(ss);
			 }
        }); 
    }
	
	//获取城市
	function citys(){
        var city = $('#cityid').val();
        $.ajax({
			type:"POST",
			url:"<?php echo U('dealersel');?>",
			dataType:"json",
			data:{'city':city},
			success:function(data){
				var ss='';
				if(data){
					for(var i=0; i<data.length; i++){
						ss+= "<option value='"+data[i]['dealer_name']+"'>"+data[i]['dealer_name']+"</option>";
					}
					$("#dels").html(ss);
				}else{
					ss+= "<option value='0'>暂无经销商</option>";
					$("#dels").html(ss).css('color','red');
				}
				
			 }
        }); 
    }
	
	
	//判断输入姓名是否正确
	function upperCase(){
		
		var name = $('#name').val();
		if(name==''){
			alert('请输入您的姓名');
			
			return false;
		}
	} 
	//判断省市
	function province1(){
		var province22 = $('#province').val();
		if(province22=='0'){
			alert('请选择省/市');
			return false;
		}
	} 
	
	
	
	//判断手机号是否正确
	function mobile1(){
		var phone = $('#phone').val();
		var myreg = '/^(((13[0-9]{1})|(15[0-9]{1})|(18[0-9]{1}))+\d{8})$/'; 
		if(!myreg.test(phone)){ 
			alert('请输入有效的手机号码！'); 
			return false; 
		} 
	
	}
	
